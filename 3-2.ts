function* circularRange(start: number, max: number){
	while(true){
		for(let i = start; i < max; i++){
			yield i;
		}
	}
}

function multiNext<T>(gen: Generator<T, any, any>, times: number): T {
	for (let i = 0; i < times-1; i++) {
		gen.next()
	}
	return gen.next().value;
}

function testSlope(xSlope: number, ySlope: number): number {
	const xPosGen = circularRange(0, input[0].length); 
	xPosGen.next();

	let xPos = 0;
	let numTrees = 0;
	for (let yPos = ySlope; yPos < input.length; yPos+=ySlope) {
		const row = input[yPos];
		xPos = multiNext(xPosGen, xSlope);
		//console.log(row);
		//console.log(`The character at ${xPos}, ${yPos} is ${row.charAt(xPos)}`);
		if(row.charAt(xPos) === "#"){
			numTrees++;
		}
	}
	return numTrees;
}

